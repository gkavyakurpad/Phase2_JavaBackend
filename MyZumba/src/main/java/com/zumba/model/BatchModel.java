package com.zumba.model;

public class BatchModel {

	private int batch_id;
	private String batch_name;
	private String batch_description;
	private String batch_timing;
	private String batch_startDate;
	
	public int getBatch_id() {
		return batch_id;
	}
	public void setBatch_id(int batch_id) {
		this.batch_id = batch_id;
	}
	public String getBatch_name() {
		return batch_name;
	}
	public void setBatch_name(String batch_name) {
		this.batch_name = batch_name;
	}
	public String getBatch_description() {
		return batch_description;
	}
	public void setBatch_description(String batch_description) {
		this.batch_description = batch_description;
	}
	public String getBatch_timing() {
		return batch_timing;
	}
	public void setBatch_timing(String batch_timing) {
		this.batch_timing = batch_timing;
	}
	public String getBatch_startDate() {
		return batch_startDate;
	}
	public void setBatch_startDate(String batch_startDate) {
		this.batch_startDate = batch_startDate;
	}
	
	
	
}
